export function useTranslation(locale: Locale = "az") {
  return {
    t: (key: keyof typeof translations.az, vars?: Record<string, any>) => {
      let str = (translations as any)[locale]?.[key] ?? String(key)

      if (vars) {
        for (const k of Object.keys(vars)) {
          str = str.replaceAll(`{{${k}}}`, String(vars[k]))
        }
      }

      return str
    },
    locale,
  }
}
